package com.example.kphisics.ui.tools

class DetailInfo {

    private var sequence = ""
    private var name = ""

    fun getSequence(): String {
        return sequence
    }

    fun setSequence(sequence: String) {
        this.sequence = sequence
    }

    fun getName(): String {
        return name
    }

    fun setName(name: String) {
        this.name = name
    }
}